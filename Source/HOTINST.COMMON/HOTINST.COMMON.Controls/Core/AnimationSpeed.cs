namespace HOTINST.COMMON.Controls.Core
{
	/// <summary>
	/// Named values for common animation duration in ms.
	/// </summary>
	public enum AnimationSpeed
	{
		/// <summary>
		/// No duration, 0.
		/// </summary>
		None = 0,
		/// <summary>
		/// Short duration, 100ms,
		/// </summary>
		Fast = 100,
		/// <summary>
		/// Normal duration, 250ms.
		/// </summary>
		Normal = 250,
		/// <summary>
		/// Long duration, 500ms.
		/// </summary>
		Slow = 500
	}
}